/* ── Helper functions ─────────────────────────── */
export function getJ1TeamById(id: string): J1Team | undefined {
  return j1Teams.find((t) => t.id === id);
}

export function getJ1TeamsByDivision(div: "EAST" | "WEST"): J1Team[] {
  return j1Teams.filter((t) => t.division === div);
}

export function getOriginal10Teams(): J1Team[] {
  return j1Teams.filter((t) => t.isOriginal10);
}
